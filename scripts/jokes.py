# A Monday, used to count elapsed weeks for the run counter.
_EPOCH_MONDAY = date(2024, 1, 1)


def get_joke(d: date | None = None) -> str:
    """Return a deterministic joke for the given date (defaults to today).

    A run counter that increments by exactly 1 per scheduled run (the two
    weekly editions: Mon–Wed → 0, Thu–Sun → 1) indexes the bank, so the
    selection walks every joke before any repeat (~len(JOKES) runs).
    """
    if d is None:
        d = date.today()
    weeks = (d - _EPOCH_MONDAY).days // 7
    edition = 0 if d.weekday() <= 2 else 1
    run_index = 2 * weeks + edition
    return JOKES[run_index % len(JOKES)]


